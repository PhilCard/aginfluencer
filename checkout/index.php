                    newPriceSpan.textContent = 'R$' + newPrice;
            
                    checkbox.id = itemId;
                    checkbox.setAttribute('data-newPrice', newPrice);
                    checkbox.setAttribute('data-description', description);
            
                    updateDescription();
                });
            });
            
